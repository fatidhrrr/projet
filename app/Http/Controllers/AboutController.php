     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, About $about)
    {
        // Condition si je ne selectionne pas de photo
        if ($request->hasFile('photo')) {
            $photo=Storage::disk('public')->put('', $request->file('photo'));
            $about->photo=$photo;
        }
        $about->soustitre=$request->input("soustitre");
        $about->titre=$request->input("titre");
        $about->description1=$request->input("description1");
        $about->description2=$request->input("description2");
        $about->save();
        return redirect()->route("about.index");
    }

